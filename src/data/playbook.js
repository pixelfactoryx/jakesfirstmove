export function getRandomPlaybookItem(excludeIds) {
  const ids = excludeIds ? new Set(excludeIds) : null;
  const options = ids
    ? PLAYBOOK.filter((item) => !ids.has(item.id))
    : [...PLAYBOOK];
  return options[Math.floor(Math.random() * options.length)];
}

export function getPlaybookItemById(id) {
  return PLAYBOOK.find((item) => item.id === id);
}
